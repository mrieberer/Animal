
public class Calculator {

	Calculator() {
	}
	
	public int addition(int firstNumber, int secondNumber) {
		return firstNumber + secondNumber;
		
	}
	
	public int subtraction(int firstNumber, int secondNumber) {
		return firstNumber - secondNumber;
	}
	
	public float addition(float firstNumber, float secondNumber) {
		return firstNumber + secondNumber;
		
	}
	
	public float subtraction(float firstNumber, float secondNumber) {
		return firstNumber - secondNumber;
	}
	
	public double addition(double firstNumber, double secondNumber) {
		return firstNumber + secondNumber;
		
	}
	
	public double subtraction(double firstNumber, double secondNumber) {
		return firstNumber - secondNumber;
	}
	
	public short addition(short firstNumber, short secondNumber) {
		return (short) (firstNumber + secondNumber);
		
	}
	
	public short subtraction(short firstNumber, short secondNumber) {
		return (short) (firstNumber - secondNumber);
	}
}
